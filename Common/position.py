class Position:

    def __init__(self, x, y, port):
        self.x = x
        self.y = y
        self.port = port

    # Returns the x
    def get_x(self):
        return self.x

    # Returns the y
    def get_y(self):
        return self.y


    # Returns the port
    def get_port(self):
        return self.port
